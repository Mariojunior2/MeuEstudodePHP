    <= testa se um valor é menor ou igual a outro valor
    ex: (10 <= 10) versadeiro

    < teste se um valor é menor que o segundo valor
    ex: (10 < 10) falso

    >= testa se um valor é maior ou igual ao segundo valor
    ex: (9 >= 20) falso

    > testa se um valor é somente maior que o segundo valor
    ex: (10 > 1) verdadeiro

    == testa se um valor é IGUAL ao segundo valor
    ex: (10 = 10) verdadeiro

    <> testa se um valor é diferente do segundo valor
    ex: (10 <> 10) falso
        (10 != 20) verdadeiro

*/    
